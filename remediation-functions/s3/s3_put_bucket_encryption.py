'''
Enable encryption on S3
'''

from botocore.exceptions import ClientError

#def run_remediation(boto_session, rule, resource):
def run_remediation(s3_client, bucket_name):
    print("Executing remediation")
    #Apply SSE Encryption with AES256 algo. to bucket
    try:
        result = s3_client.put_bucket_encryption(
            Bucket = bucket_name,
            ServerSideEncryptionConfiguration = {
                'Rules': [
                    {
                        'ApplyServerSideEncryptionByDefault': {
                            'SSEAlgorithm': 'AES256'
                        }
                    },
                ]
            }
        )

        responseCode = result['ResponseMetadata']['HTTPStatusCode']
        if responseCode >= 400:
            output = "Unexpected error: %s \n" % str(result)
        else:
            output = "Bucket encryption enabled: %s \n" % bucket_name

    except ClientError as e:
        responseCode = 400
        output = "Unexpected error: " + str(e)
        print(output)
    except Exception as e:
        responseCode = 400
        output = "Unexpected error: " + str(e)
        print(output)

    print(str(responseCode)+'-'+output)   
    return responseCode,output